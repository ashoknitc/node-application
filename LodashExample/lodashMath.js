var _ = require('lodash');
console.log("Addition Of 10 and 20 : " + _.add(20, 30));
console.log("large Value =" + _.ceil(12.57));
console.log("Small Value =" + _.floor(12.57));
console.log("Division Value =" + _.divide(12, 2));
console.log("max value of Array List =" + _.max([12, 11, 10]));
console.log("Chunk Example =" + _.chunk([12, 11, 10, 9, 7, 5, 8], 2));